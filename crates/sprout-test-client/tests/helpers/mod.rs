#![allow(dead_code)]
//! Shared test helpers for sprout-test-client integration tests.
//!
//! Import with:
//! ```rust
//! mod helpers;
//! use helpers::*;
//! ```
//!
//! Note: URL helpers are intentionally NOT included here because different test
//! suites target different default ports (3000 vs 3001) and different env vars
//! (RELAY_URL vs RELAY_HTTP_URL). Each test file keeps its own URL helpers.

use std::time::Duration;

use base64::{engine::general_purpose::URL_SAFE_NO_PAD, Engine as _};
use nostr::{EventBuilder, JsonUtil, Keys, Kind, Tag, Timestamp};
use reqwest::Client;

// ── HTTP client ───────────────────────────────────────────────────────────────

/// Build a `reqwest::Client` with a 15-second timeout.
pub fn http_client() -> Client {
    Client::builder()
        .timeout(Duration::from_secs(15))
        .build()
        .expect("failed to build HTTP client")
}

// ── Subscription ID ───────────────────────────────────────────────────────────

/// Generate a unique subscription ID for a named test.
pub fn sub_id(name: &str) -> String {
    format!("e2e-{name}-{}", uuid::Uuid::new_v4())
}

// ── Channel creation ──────────────────────────────────────────────────────────

/// Create a real channel via a signed kind:9007 event submitted to POST /api/events.
///
/// `base_url` is the HTTP relay URL (e.g. `http://localhost:3000`).
/// `prefix` is used as the channel name prefix (e.g. `"relay-e2e"` or `"interop-e2e"`).
///
/// Returns the new channel's UUID string.
pub async fn create_test_channel(keys: &Keys, base_url: &str, prefix: &str) -> String {
    let client = Client::new();
    let pubkey_hex = keys.public_key().to_hex();
    let channel_uuid = uuid::Uuid::new_v4();
    let channel_name = format!("{prefix}-{channel_uuid}");

    let event = EventBuilder::new(
        Kind::Custom(9007),
        "",
        vec![
            Tag::parse(&["h", &channel_uuid.to_string()]).unwrap(),
            Tag::parse(&["name", &channel_name]).unwrap(),
            Tag::parse(&["channel_type", "stream"]).unwrap(),
            Tag::parse(&["visibility", "open"]).unwrap(),
        ],
    )
    .sign_with_keys(keys)
    .unwrap();

    let resp = client
        .post(format!("{base_url}/api/events"))
        .header("X-Pubkey", &pubkey_hex)
        .header("Content-Type", "application/json")
        .body(serde_json::to_string(&event).unwrap())
        .send()
        .await
        .expect("submit create-channel event");
    assert!(
        resp.status().is_success(),
        "channel creation event failed: {}",
        resp.status()
    );
    let body: serde_json::Value = resp.json().await.expect("parse event response");
    assert!(
        body["accepted"].as_bool().unwrap_or(false),
        "channel creation not accepted: {body}"
    );

    channel_uuid.to_string()
}

// ── Blossom auth helpers ──────────────────────────────────────────────────────

/// Sign a kind:24242 Blossom upload auth event for the given sha256.
pub fn sign_blossom_auth(keys: &Keys, sha256: &str) -> nostr::Event {
    let now = Timestamp::now().as_u64();
    let exp_str = (now + 300).to_string();
    let tags = vec![
        Tag::parse(&["t", "upload"]).expect("t tag"),
        Tag::parse(&["x", sha256]).expect("x tag"),
        Tag::parse(&["expiration", &exp_str]).expect("expiration tag"),
    ];
    EventBuilder::new(Kind::from(24242), "Upload test", tags)
        .sign_with_keys(keys)
        .expect("sign blossom auth")
}

/// Build `Authorization: Nostr <base64url(json)>` header value.
pub fn blossom_auth_header(event: &nostr::Event) -> String {
    format!(
        "Nostr {}",
        URL_SAFE_NO_PAD.encode(event.as_json().as_bytes())
    )
}

